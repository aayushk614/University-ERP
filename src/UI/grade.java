    private void gradeTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gradeTableMouseClicked
        int row = gradeTable.getSelectedRow();
        int col = gradeTable.getColumnCount();

        Object[] data = new Object[col];
        for (int i=0; i<col; i++)
            data[i] = gradeTable.getValueAt(row, i);

        enrollid.setText(data[0].toString());
        studentid.setText(data[1].toString());
        courseid.setText(data[2].toString());
        grade.setText(data[3].toString());
        numericgrade.setText(data[4].toString());
        
        
    }//GEN-LAST:event_gradeTableMouseClicked


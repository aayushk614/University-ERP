    // Method to load data into the table
    public void loadDataSet() {
        try {
            EnrollmentsDAO enrollmentsDAO = new EnrollmentsDAO();
            gradeTable.setModel(enrollmentsDAO.buildTableModel(enrollmentsDAO.getQueryResult()));
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    // Method to display search result in table
    public void loadSearchData(String text) {
        try {
            EnrollmentsDAO facultyDAO = new EnrollmentsDAO();
            gradeTable.setModel(facultyDAO.buildTableModel(facultyDAO.getSearchResult(text)));
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }


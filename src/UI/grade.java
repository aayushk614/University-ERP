    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
       if (enrollid.getText().equals("") || studentid.getText().equals("")
                || courseid.getText().equals("") || grade.getText().equals("") || numericgrade.getText().equals(""))
            JOptionPane.showMessageDialog(this, "Please enter all the required details.");
        else {
            EnrollmentsDTO customerDTO = new EnrollmentsDTO();
            customerDTO.setEnrollmentID(Integer.parseInt(enrollid.getText()));
            customerDTO.setStudentID(studentid.getText());
            customerDTO.setCourseID(courseid.getText());
            customerDTO.setGrade(grade.getText());
            customerDTO.setNumericGrade(Integer.parseInt(numericgrade.getText()));
            EnrollmentsDAO obj=new EnrollmentsDAO();
            obj.addEnrollmentsDAO(customerDTO);
            loadDataSet();
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
if (gradeTable.getSelectedRow()<0)
            JOptionPane.showMessageDialog(this, "Select a customer from the table.");
        else {
            int opt = JOptionPane.showConfirmDialog(
                    this,
                    "Are you sure you want to delete this Student?",
                    "Confirmation",
                    JOptionPane.YES_NO_OPTION);
            if (opt==JOptionPane.YES_OPTION) {
                new EnrollmentsDAO().deleteEnrollmentDAO(gradeTable.getValueAt(gradeTable.getSelectedRow(),0).toString());
                loadDataSet();
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

